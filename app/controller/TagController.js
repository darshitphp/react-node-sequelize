const TagModel = require('../model/TagModel');

exports.FindAllData = (req, resp) => {
  TagModel.getAllTagData(req)
    .then((result) => {
      resp.send({
        status: "success",
        code: "200",
        message: "All Data from Many to Many",
        data: result,
      });
    })
    .catch((error) => {
      resp.send({
        status: "error",
        code: "500",
        message: error,
      });
    });
};

